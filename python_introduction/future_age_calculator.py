# This code calculates the future age of a person in the year 2050 based on their current age.
# It prompts the user for their current age, adds 27 years to it (assuming the current year is 2023), and then prints the future age.
# The calculation is straightforward: current age + 27 = future age in 2050.

age = int(input("How old are you? ")) + 27
print(f"In 2050, you will be {age} years old.")
